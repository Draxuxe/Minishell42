#include <strings.h>

void	ft_bzero(void *s, size_t n)
{
	unsigned long	index;
	unsigned char	*tmp;

	index = 0;
	tmp = (unsigned char *) s;
	while (index < n)
	{
		tmp[index] = '\0';
		index++;
	}
}
